package com.jaya.demo;

public class User {
	
	int x;
	int y;
	Calc calc;
	
	public void invokeAdd(int x,int y) {
		
		calc.add(x,y);
	}
    
	public Calc getCalc() {
		return getCalc();
	}
	
	public int getX() {
		return x;
	}
	
	public void setx(int x) {
		this.x=x;
	}
	
	public int getY() {
		return y;
	}
	
	public void setY(int y) {
		this.y=y;
	}
	
	
	
}

